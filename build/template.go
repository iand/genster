package build

import (
	"embed"
	"fmt"
	"html/template"
	"strings"
	"time"

	"github.com/iand/genster/layout"
)

//go:embed templates
var templateFS embed.FS

// urlize converts a string to a URL-safe slug: lowercase, spaces → hyphens.
// It is used both in templates (via templateFuncs) and directly in Go code
// wherever tag slugs are needed, keeping the two in sync.
func urlize(s string) string {
	s = strings.ToLower(s)
	s = strings.ReplaceAll(s, " ", "-")
	return s
}

// buildSiteTemplates creates a fresh template set for one Build() run.
// imageDir is the filesystem path to the images/ subdirectory of the content
// root. The featureImageSrc template function closes over this path and calls
// SelectFeatureImage to resolve the best available image for each page.
func buildSiteTemplates(imageDir string) *template.Template {
	funcs := template.FuncMap{
		"urlize": urlize,
		// ukdate formats a YYYY-MM-DD date string as "2 January 2006".
		// Returns the original string unchanged if it cannot be parsed.
		"ukdate": func(s string) string {
			t, err := time.Parse("2006-01-02", s)
			if err != nil {
				return s
			}
			return t.Format("2 January 2006")
		},
		// featureImageSrc resolves the best available feature image URL for a
		// page based on its front-matter. It returns an empty string when no
		// suitable image is found so the template can suppress the <img> tag.
		"featureImageSrc": func(fm FrontMatter) string {
			return SelectFeatureImage(fm, imageDir)
		},
	}
	return template.Must(template.New("").Funcs(funcs).ParseFS(templateFS, "templates/*.html"))
}

// knownLayouts is the set of layout values that genster and manual content
// files may use. An unrecognised layout causes an error at build time so
// misconfigured files surface immediately rather than silently rendering blank.
var knownLayouts = map[string]bool{
	// Generated tree pages — values must match the layout package constants.
	layout.PageLayoutPerson.String():          true,
	layout.PageLayoutFamily.String():          true,
	layout.PageLayoutPlace.String():           true,
	layout.PageLayoutCitation.String():        true,
	layout.PageLayoutSource.String():          true,
	layout.PageLayoutListPeople.String():      true,
	layout.PageLayoutListSurnames.String():    true,
	layout.PageLayoutListPlaces.String():      true,
	layout.PageLayoutListSources.String():     true,
	layout.PageLayoutListAnomalies.String():   true,
	layout.PageLayoutListInferences.String():  true,
	layout.PageLayoutListTodo.String():        true,
	layout.PageLayoutListFamilies.String():    true,
	layout.PageLayoutListFamilyLines.String(): true,
	layout.PageLayoutListTrees.String():       true,
	layout.PageLayoutListChanges.String():     true,
	layout.PageLayoutCalendar.String():        true,
	layout.PageLayoutChartAncestors.String():  true,
	layout.PageLayoutTreeOverview.String():    true,
	layout.PageLayoutChartTrees.String():      true,
	// Manual content layouts (not generated by the site package).
	"home":   true,
	"search": true,
	"single": true,
	"list":   true,
	"diary":  true,
	// No layout specified (plain content files)
	"": true,
}

// selectTemplate returns the named template to render the given layout. An
// unrecognised layout returns an error that identifies both the source file
// and the bad layout value.
func selectTemplate(tmpls *template.Template, layout, srcPath string) (*template.Template, error) {
	if !knownLayouts[layout] {
		return nil, fmt.Errorf("unknown layout %q in %s", layout, srcPath)
	}

	// Map the empty layout to the "plain" template.
	name := layout
	if name == "" {
		name = "plain"
	}

	tmpl := tmpls.Lookup(name)
	if tmpl == nil {
		// All knownLayouts should have matching templates; this indicates a
		// programming error (template file missing or define block misspelled).
		return nil, fmt.Errorf("no template found for layout %q in %s", layout, srcPath)
	}
	return tmpl, nil
}
